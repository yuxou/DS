#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

typedef int element;
typedef struct TreeNode {
	element key;
	struct TreeNode* left, * right;
} TreeNode;

TreeNode* new_node(int item)
{
	TreeNode* temp = (TreeNode*)malloc(sizeof(TreeNode));
	temp->key = item;
	temp->left = temp->right = NULL;
	return temp;
}

TreeNode* insert_node(TreeNode* node, int key)
{
	// 트리가 공백이면 새로운 노드를 반환한다. 
	if (node == NULL) return new_node(key);

	// 그렇지 않으면 순환적으로 트리를 내려간다. 
	if (key < node->key)
		node->left = insert_node(node->left, key);
	else if (key > node->key)
		node->right = insert_node(node->right, key);

	// 변경된 루트 포인터를 반환한다. 
	return node;
}
TreeNode* min_value_node(TreeNode* node)
{
	TreeNode* current = node;

	// 맨 왼쪽 단말 노드를 찾으러 내려감
	while (current->left != NULL)
		current = current->left;

	return current;
}
// 이진 탐색 트리와 키가 주어지면 키가 저장된 노드를 삭제하고 
// 새로운 루트 노드를 반환한다. 
TreeNode* delete_node(TreeNode* root, int key)
{
	if (root == NULL) return root;

	// 만약 키가 루트보다 작으면 왼쪽 서브 트리에 있는 것임
	if (key < root->key)
		root->left = delete_node(root->left, key);
	// 만약 키가 루트보다 크면 오른쪽 서브 트리에 있는 것임
	else if (key > root->key)
		root->right = delete_node(root->right, key);
	// 키가 루트와 같으면 이 노드를 삭제하면 됨
	else {
		// 첫 번째나 두 번째 경우
		if (root->left == NULL) {
			TreeNode* temp = root->right;
			free(root);
			return temp;
		}
		else if (root->right == NULL) {
			TreeNode* temp = root->left;
			free(root);
			return temp;
		}
		// 세 번째 경우
		TreeNode* temp = min_value_node(root->right);

		// 중외 순회시 후계 노드를 복사한다. 
		root->key = temp->key;
		// 중외 순회시 후계 노드를 삭제한다. 
		root->right = delete_node(root->right, temp->key);
	}
	return root;
}

// 순환적인 탐색 함수
TreeNode* search(TreeNode* node, int key)
{
	if (node == NULL) return NULL;
	if (key == node->key) return node;
	else if (key < node->key)
		return search(node->left, key);
	else
		return search(node->right, key);
}

void print_tree(TreeNode* root)
{
	if (root != NULL) {
		printf("%d ", root->key); //노드 방문
		print_tree(root->left); //왼쪽 서브트리 순회
		print_tree(root->right); //오른쪽 서브트리 순회
	}
}

int get_height(TreeNode* node)
{
	int height = 0;

	if (node != NULL)
		height = 1 + max(get_height(node->left), 
			get_height(node->right));

	return height;
}

int get_node_count(TreeNode* node)
{
	int count = 0;

	if (node != NULL)
		count = 1 + get_node_count(node->left) +
		get_node_count(node->right);

	return count;
}

int get_maximum(TreeNode* node)
{
	TreeNode* current = node;

	while (current->right != NULL)
		current = current->right;

	return current->key;
}

int get_minimum(TreeNode* node)
{
	TreeNode* current = node;

	while (current->left != NULL)
		current = current->left;

	return current->key;
}

void alert()
{
	printf("Enter i<nsert>, d<elete>, s<earch>, p<rint>, h<eight>, c<ount>, m<ax>, n<min>, q<uit>: ");
}

int main(void)
{
	TreeNode* root = NULL;
	TreeNode* tmp;
	char answer;
	element e;

	do {
		alert();
		answer = getchar();
		getchar();

		switch (answer) {
		case 'i': 
			printf("삽입할 key값 입력: ");
			scanf("%d", &e);
			root = insert_node(root, e);
			while (getchar() != '\n');
			break;
		case 'd':
			printf("삭제할 key값 입력: ");
			scanf("%d", &e);
			root = delete_node(root, e);
			while (getchar() != '\n');
			break;		
		case 's':
			printf("탐색할 key값 입력: ");
			scanf("%d", &e);
			tmp = search(root, e);
			if (tmp != NULL)
				printf("있음\n");
			else
				printf("없음\n");
			while (getchar() != '\n');
			break;
		case 'p':
			print_tree(root);
			printf("\n");
			break;
		case 'h':
			printf("트리의 높이는 %d\n", get_height(root));
			break;
		case 'c':
			printf("노드의 개수는 %d\n", get_node_count(root));
			break;		
		case 'm':
			printf("가장 큰 값은 %d\n", get_maximum(root));
			break;
		case 'n':
			printf("가장 작은 값은 %d\n", get_minimum(root));
			break;
		}
	} while (answer != 'q');

	return 0;
}